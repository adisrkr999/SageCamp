package JavaFunctions;

public class FunctionsContinued {
    public static void main(String[] args) {
        int sum =0;
        for(int num =1;num<=10;num++){
            sum = sum +num;
        }
        System.out.println("Sum of numbers from 1 to 10 is "+sum);

        sum =0;
        for(int num =20;num<=38;num++){
            sum = sum +num;
        }
        System.out.println("Sum of numbers from 20 to 38 is "+sum);

        sum =0;
        for(int num =30;num<=50;num++){
            sum = sum +num;
        }
        System.out.println("Sum of numbers from 30 to 50 is "+sum);
    }

}
